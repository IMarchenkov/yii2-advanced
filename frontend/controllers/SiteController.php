    /**
     * Displays homepage.
     *
     * @return mixed
     */
//    public function actionIndex()
//    {
//        return $this->render('index');
//    }
    public function actionIndex()
    {
        Yii::$app->cache->flush();
        if (!Yii::$app->user->isGuest) {

            $userId = Yii::$app->user->id;
            $key = 'task_current_task_' . $userId;

            $cache = Yii::$app->cache;

            $dataProvider = $cache->get($key);

            if (!$dataProvider) {

                $dataProvider = new ActiveDataProvider([
                    'query' => Project::findCurrentTask(),
//                    'pagination' => [
//                        'pageSize' => 5
//                    ],
                ]);

                $dependency = new DbDependency();
                $dependency->sql = "SELECT COUNT(*) FROM task WHERE initiator_id = " . Yii::$app->user->id . " OR responsible_id = " . Yii::$app->user->id;

                $dataProvider->prepare();

                $cache->set($key, $dataProvider, 3600, $dependency);
            }

            return $this->render('index', [
                'dataProvider' => $dataProvider,
            ]);
        }

        $model = new LoginForm();
        if ($model->load(Yii::$app->request->post()) && $model->login()) {
            return $this->goBack();
        }

        $model->password = '';
        return $this->render('login', [
            'model' => $model,
        ]);
    }

    /**
     * Logs in a user.
     *
     * @return mixed
     */
    public function actionLogin()
    {
        if (!Yii::$app->user->isGuest) {
            return $this->goHome();
        }

        $model = new LoginForm();
        if ($model->load(Yii::$app->request->post()) && $model->login()) {
            return $this->goBack();
        } else {
            $model->password = '';

            return $this->render('login', [
                'model' => $model,
            ]);
        }
    }

    /**
     * Logs out the current user.
     *
     * @return mixed
     */
    public function actionLogout()
    {
        Yii::$app->user->logout();

        return $this->goHome();
    }

    /**
     * Displays contact page.
     *
     * @return mixed
     */
    public function actionContact()
    {
        $model = new ContactForm();
        if ($model->load(Yii::$app->request->post()) && $model->validate()) {
            if ($model->sendEmail(Yii::$app->params['adminEmail'])) {
                Yii::$app->session->setFlash('success', 'Thank you for contacting us. We will respond to you as soon as possible.');
            } else {
                Yii::$app->session->setFlash('error', 'There was an error sending your message.');
            }

            return $this->refresh();
        } else {
            return $this->render('contact', [
                'model' => $model,
            ]);
        }
    }

